# src/mt4_bridge/strategies/bollinger_trend_B2_params.py
"""bollinger_trend_B2 (新世代 B 戦術) のパラメータ。

設計原則:
- partial close なし、1 ポジション = 1 エントリー〜1 決済
- 戦術ロジックで入口・早期撤退・TP を完全に握る(signal_close 主体)
- simulator 側 SL は保険のみ(15 pips)、TP は戦術ロジックが打てない時の保険(100 pips)
"""
from __future__ import annotations


# ===================================================================
# EMA (短期 momentum)
# ===================================================================
EMA_FAST_PERIOD = 9
EMA_SLOW_PERIOD = 21

# ===================================================================
# ADX (trend 強度) — レンジ排除のため
# ===================================================================
ADX_PERIOD = 14
ADX_THRESHOLD_ENTRY = 25.0  # 入口条件 2: ADX > 25 (旧 20 から厳格化、2024 OOS で調整)
ADX_THRESHOLD_EXIT = 15.0   # 早期撤退 C: ADX < 15 で trend 消滅と判断し CLOSE

# ===================================================================
# ATR (ボラティリティ正規化)
# ===================================================================
ATR_PERIOD = 14

# ===================================================================
# H1 擬似足合成 (5分足 × 12 = 60分)
# ===================================================================
H1_BARS = 12                       # 5分足 12 本で H1 1 本を合成
H1_EMA_PERIOD = 20                 # H1 ベース EMA
H1_SLOPE_LOOKBACK = 3              # EMA の slope を見る bar 数
H1_SLOPE_ATR_RATIO = 0.10          # EMA slope を ATR で正規化してこの比率超えで trend 方向明確

# ===================================================================
# 入口 (PullBack entry)
# ===================================================================
# 条件 4: EMA(21) からの価格距離が ATR × この比率以内ならタッチ戻りと見なす
PULLBACK_ATR_RATIO = 0.30

# 条件 6 (確認バー): 現 bar の price action が trend 方向と一致すること
# buy: close > open (陽線), sell: close < open (陰線)
# "落ちるナイフを掴む" 防止、pullback が反転し始めた bar で入る
CONFIRMATION_CANDLE_ENABLED = True

# ===================================================================
# 早期撤退
# ===================================================================
# 2024 OOS 調整で 3bar/+5pips → 5bar/+3pips に緩和
# (pullback が浅いだけで切りすぎている噛まされを減らす)
EARLY_EXIT_BARS = 5                # 条件 A: エントリー後 N bar 以内に
EARLY_EXIT_MIN_PROFIT_PIPS = 3.0   #         含み益が +3 pips に達しなければ CLOSE

# ===================================================================
# TP 判断
# ===================================================================
# 条件 I: 反対側 BB 2σ タッチで TP
BOLLINGER_PERIOD = 20
BOLLINGER_SIGMA = 2.0

# 条件 III: 含み益がこの値に達し、その後 N bar 内に新高値/新安値更新なしで TP
TP_PROFIT_THRESHOLD_PIPS = 30.0
TP_STALL_BARS = 5

# 条件 IV: 保有時間が N bar を超えたら利益が乗っていれば TP
TIME_EXIT_BARS = 72                # 5m × 72 = 6 時間

# 条件 V: Peak-trail TP (B3 の学び移植、2024 OOS で 2σ タッチ未到達の大伸びを救済)
# MFE が PEAK_TRAIL_TRIGGER_PIPS に到達したら "peak 追跡" 開始、
# 以降 peak から GIVEBACK 戻りで CLOSE
#
# 2024 OOS スキャン (10/15/20/30 の比較) で T30/G0.50 が最良:
# - T10: 発動 48 件、通常の 2σ タッチを奪って取り逃し悪化 (+1.8)
# - T30: 発動わずか 4 件、+30 超の大伸び例外だけ救済 (+114.7) ← 新記録
# 通常のトレードは 2σ タッチ (avg +12-16) でそのまま取れるよう維持
PEAK_TRAIL_TRIGGER_PIPS = 30.0
PEAK_TRAIL_GIVEBACK_RATIO = 0.50

# ===================================================================
# 通貨ペア/Pip 計算
# ===================================================================
PIP_MULTIPLIER = 100.0             # USDJPY は 0.01 = 1 pip

# ===================================================================
# リスク定数 (戦術ファイル正本、simulator の fallback として参照される)
# ===================================================================
SL_PIPS = 15.0
TP_PIPS = 100.0

# ===================================================================
# Magic (combo 拡張時の識別用)
# ===================================================================
TREND_B2_MAGIC_NUMBER = 44003


def required_bars() -> int:
    """この戦術が評価のために必要とする最小バー本数。

    H1 EMA slope には H1 EMA が少なくとも H1_SLOPE_LOOKBACK+1 本必要。
    H1 EMA が N 本作られるには H1 close が H1_EMA_PERIOD + N - 1 本必要。
    よって 5 分足換算で H1_BARS × (H1_EMA_PERIOD + H1_SLOPE_LOOKBACK) + margin。
    """
    return max(
        EMA_SLOW_PERIOD + 2,
        ADX_PERIOD * 2 + 2,
        ATR_PERIOD + 2,
        BOLLINGER_PERIOD + 2,
        H1_BARS * (H1_EMA_PERIOD + H1_SLOPE_LOOKBACK) + 2,
    )
