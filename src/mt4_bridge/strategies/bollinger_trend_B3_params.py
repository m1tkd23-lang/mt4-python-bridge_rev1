# src/mt4_bridge/strategies/bollinger_trend_B3_params.py
"""bollinger_trend_B3: ボリンジャーバンド主体の新世代 B 戦術のパラメータ。

設計の骨子:
- 入口: BB 幅が直前 bar より拡大 + ミドル交差で trend 開始方向にエントリー
- 早期撤退: 反対側 2σ タッチ (想定外逆行の損切り)
- TP: 順方向 3σ タッチ / 数本先のミドル戻り / 固定 TP (保険)
- partial close なし、1 position = 1 エントリー〜1 決済
"""
from __future__ import annotations


# ===================================================================
# Bollinger Bands
# ===================================================================
BOLLINGER_PERIOD = 20
BOLLINGER_SIGMA = 2.0            # 逃げラインに使う 2σ
BOLLINGER_EXTREME_SIGMA = 3.0    # TP に使う 3σ (伸び切り確定)

# ===================================================================
# 入口 (BB 拡大 + ミドル交差)
# ===================================================================
# BB 幅の拡大判定: 最新 band_width が prev band_width × この比率を超えているか
# 1.0 ちょうどだとノイズで入りやすい。1.02 以上で明確な拡大のみ
# 2024 OOS テスト: 1.02 → 1.05 で -40 まで改善、1.08 にすると -338 に悪化
# (強すぎる拡大は "動き切った後" でエントリーするため伸び代がない)
# 1.05 が sweet spot として採用
BANDWIDTH_EXPANSION_RATIO = 1.05

# ===================================================================
# TP 判断
# ===================================================================
# 数本先のミドル戻り判定: エントリー後 N bar 以上経過したら評価開始
MIDDLE_REVERT_MIN_BARS = 3

# ===================================================================
# Peak-trail TP (MFE ベースの疑似 trailing)
# ===================================================================
# MFE が PEAK_TRAIL_TRIGGER_PIPS に到達したら "peak 追跡モード" 開始、
# その後 peak から (peak * PEAK_TRAIL_GIVEBACK_RATIO) だけ戻ったら CLOSE
PEAK_TRAIL_TRIGGER_PIPS = 10.0
PEAK_TRAIL_GIVEBACK_RATIO = 0.50

# ===================================================================
# MAE 早切り (損小、反対 2σ を待たずに負けを確定)
# ===================================================================
# 含み損 (= MAE の深さ) がこの pips を超えたら CLOSE
# 9999 = 実質廃止 (2024 OOS 分析で、MAE cut が MFE 10 到達候補を潰していたため)
# 逃げは反対 2σ 接触 と simulator SL=25 に委ねる
MAE_EARLY_CUT_PIPS = 9999.0

# ===================================================================
# 時間ベース逃げ (含み益未達の長期保有を撤退)
# ===================================================================
# エントリー後 N bar 経過しても含み益が上がってこなければ、
# 反対 2σ を待たず損失が拡大する前に CLOSE する。
# 2024 OOS スキャン (8/12/16/20/24/無効) で 12bar が sweet spot:
#   - 8bar は切り過ぎ (tp 候補まで潰す、wr 45.4%)
#   - 16bar+ は time_exit の効果が薄まる
#   - 12bar: total -18.2 / wr 49.2% / crash 5 (B3 最良)
TIME_EXIT_STAGNANT_BARS = 12         # 経過バー数閾値
TIME_EXIT_STAGNANT_MIN_PIPS = 0.0    # この pips 以下なら "含み益未達" と判定

# ===================================================================
# Pip 設定
# ===================================================================
PIP_MULTIPLIER = 100.0   # USDJPY

# ===================================================================
# リスク (戦術ファイル正本、simulator fallback)
# ===================================================================
# SL: 2σ タッチで逃げが効くことを想定、simulator 保険はやや広め
# TP: 戦術が打つので固定 TP も保険扱い(長めに設定)
SL_PIPS = 25.0
TP_PIPS = 80.0

# ===================================================================
# Magic (combo 拡張時の識別用)
# ===================================================================
TREND_B3_MAGIC_NUMBER = 44004


def required_bars() -> int:
    """この戦術が評価のために必要とする最小バー本数。

    BB を 2 回(最新と前 bar)計算するため +1、余裕で +2。
    """
    return BOLLINGER_PERIOD + 3
